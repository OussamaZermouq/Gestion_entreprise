    public void ajouter_Depot(int id,String nom,String adresse,String tele) throws SQLException {
        int count = db_connection.execute_query("Select count(*) as count from depot where id_depot="+id).getInt("count");
        if (count == 0&& (!String.valueOf(id).equals("") )){
            Depot depot= new Depot(id, nom , adresse,tele);
            depots.add(depot);
            String query = "Insert into depot values('"+depot.id+"', '"+depot.nom_depot+"','"+depot.adresse_depot+"','"+depot.tel_depot+"')";
            System.out.println(query);
            db_connection.execute_query_UD(query);
            jTable1.setModel(remplir_jtable());
            JOptionPane.showMessageDialog(null, "Depot ajouter!");
        }
        else{
            JOptionPane.showMessageDialog(null, "Cette id existe deja.");
            return;
        }
    }
    public int find_depot_in_list(int id) {
        for (int i = 0; i < depots.size(); i++) {
            if (depots.get(i).id == id) {
                return i;
            }
        }
        return -1;
    }
