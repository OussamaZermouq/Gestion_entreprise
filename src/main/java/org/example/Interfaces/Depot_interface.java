    public void modifier_depot(int id_depot,String nom_depot,String adresse_depot,String tel_depot) throws SQLException{

        if (find_depot_in_list(id_depot)>-1){

            Depot depot_mod= new Depot(id_depot, nom_depot, adresse_depot, tel_depot);
            //replace the new product in the products list using the index of the found product
            depots.set(find_depot_in_list(id_depot), depot_mod);
            String query = "Update depot set nom_depot ='"+depot_mod.nom_depot+"', adresse_depot = '"+depot_mod.adresse_depot+"', tel_depot ='"+depot_mod.tel_depot+"', where id_depot='"+depot_mod.id+"'";
            depots=Depot.get_all_depot();
            db_connection.execute_query(query);
            jTable1.setModel(remplir_jtable());
            JOptionPane.showMessageDialog(null, "depot modifier!");
        }
        else{
            JOptionPane.showMessageDialog(null, "Cette id n'existe pas!");
        }
    }
    public void supprimer_depot(int id) throws SQLException{
        if (find_depot_in_list(id)>-1){
            String query = "Delete from depot where id_depot='"+id+"'";
            int out = db_connection.execute_query_UD(query);

            if (out >0){
                depots.remove(find_depot_in_list(id));

                depots= Depot.get_all_depot();

                jTable1.setModel(remplir_jtable());
                JOptionPane.showMessageDialog(null, "Depot supprimer!");
            }
            else {
                JOptionPane.showMessageDialog(null, "Aucun depot n'a ete supprimer!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Cette id n'existe pas!");
        }
    }


    public static ArrayList<Depot> remplir_list(){
        try {
            depots = Depot.get_all_depot();
        } catch (SQLException e) {
            throw new RuntimeException(e);
        }

        return depots;
    }
